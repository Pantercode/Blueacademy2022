#Interação do úsuario#
alfa = input('Digite algo: ')

#Definiva o primitivos#
print('O tipo primitivo desse valor é ?', type(alfa))
print('O tipo tem espaços ? ', alfa.isspace())
print('Só tem números?', alfa.isnumeric())
print('É alfanúmerico ?', alfa.isalnum())
print('É alfabético ?', alfa.isalpha ())
print('Está em letras maiúsculas ?', alfa.isupper())
print('Existe letras minúsculas ?', alfa.islower())
print('Ésta capitalizada ?', alfa.istitle())



