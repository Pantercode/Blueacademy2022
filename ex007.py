#Dobro,Triplo e Raiz quadrada#
#Interaçao do Usuário#
n = int(input('Digite um numero: '))
#Resolução do problema#
d = n * 2
t = n * 3
r = n **(1/2)
print('O Dobro do número {} resulta em {}.'.format(n,d))
print('O Triplo do número {} resulta em {}.'.format(n,t))
print('A Raiz quadrada do número {} resulta em {}.'.format(n,r))


