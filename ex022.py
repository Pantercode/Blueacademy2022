frase = str(input('Digite uma frase: ')).upper()
print('A letra A apareceu {} vezes!'.format(frase.count('O')))

print('A primeira letra O apareceu na {}'.format(frase.find('O')+1))

print('A ultima letra O apareceu na {} .'.format(frase.rfind('O')+1))


